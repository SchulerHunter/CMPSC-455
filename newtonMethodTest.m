maxIter = 100;
tol1 = 10^-5;
tol2 = 10^-6;
tol3 = 10^-8;
tol4 = 10^-10;
newtonMethod(f1, fp1, x0, tol1, maxIter);
newtonMethod(f2, fp2, x0, tol1, maxIter);
